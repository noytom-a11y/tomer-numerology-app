def reduce_number(n, special_rules=True, reduce_all=False):
    """××¦××¦× ××¡×¤×¨. special_rules=True ×©×•××¨ ×¢×œ ×××¡×˜×¨×™× ×•×§××¨××”. reduce_all=True ×ª××™×“ ××¦××¦× ×œ×—×“-×¡×¤×¨×ª×™."""
